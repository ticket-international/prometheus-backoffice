export function calculateBannerStats(banner: BannerCampaign) {
  const ctr = banner.impressions > 0 ? (banner.clicks / banner.impressions) * 100 : 0;
  const conversionRate = banner.clicks > 0 ? (banner.conversions / banner.clicks) * 100 : 0;
  const cpc = banner.clicks > 0 ? banner.budget / banner.clicks : 0;
  const roas = banner.budget > 0 ? banner.revenue / banner.budget : 0;

  return {
    ctr,
    conversionRate,
    cpc,
    roas,
  };
}
